    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        UsuarioController usuarioController = new UsuarioController();
        Usuario usuario = new Usuario();
        usuario.setLogin(tfLogin.getText());
        usuario.setSenha(tfSenha.getText());
        if(usuarioController.verificaLoginUsuario(usuario)){
            usuario = new Usuario();
            usuario = usuarioController.buscar(tfLogin.getText());
            
            try {
                if(!usuarioController.temUsuarioLogado())
                    usuarioController.adicionarUsuarioLogado(usuario);
            } catch (SQLException ex) {
                Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            ViewPrincipal v = new ViewPrincipal();
            v.tfUsuario.setText("Bem vindo, " + usuario.getLogin().toUpperCase());
            v.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Usuario n√£o existe.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

