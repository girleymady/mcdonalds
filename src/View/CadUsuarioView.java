    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        usuario.setLogin(tfLogin.getText());
        usuario.setSenha(tfSenha.getText());
        usuario.setNome(tfNome.getText());
        usuario.setTipo(jComboBox1.getSelectedItem().toString().equals("ADMIN") ? 1 : 2);
        usuario = usuarioController.cadastrar(usuario);
        if (usuario != null) {
            JOptionPane.showMessageDialog(null, "Usu치rio (" + usuario.getNome().toUpperCase() + ") foi cadastrado com sucesso.");
            limparCampos();
            listar();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Boolean deletado = usuarioController.deletar(Integer.parseInt(tfId.getText()));
        if(deletado) {
            JOptionPane.showMessageDialog(null, "Usu치rio (" + tfNome.getText() + ") foi deletado com sucesso.");
            limparCampos();
            listar();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        usuario.setLogin(tfLogin.getText());
        usuario.setSenha(tfSenha.getText());
        usuario.setNome(tfNome.getText());
        usuario.setTipo(jComboBox1.getSelectedItem().toString().equals("ADMIN") ? 1 : 2);
        usuario.setId(Integer.parseInt(tfId.getText()));
        Boolean atualizacao = usuarioController.atualizar(usuario);
        if (atualizacao) {
            JOptionPane.showMessageDialog(null, "Usu치rio (" + usuario.getNome().toUpperCase() + ") foi atualizado com sucesso.");
            limparCampos();
            listar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

