    private void listar() { 
        listar = vendaController.listar();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setNumRows(0);
        int cont = listar.size();
        for(int i = 0; i <cont; i++){
            model.addRow(new Object[]{
            listar.get(i).getCodigo(),
            listar.get(i).getNomeProduto(),
            listar.get(i).getQuantidade(),
            "R$" + listar.get(i).getValor()
        });
        }
    }

